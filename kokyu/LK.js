function stage()
{
  //tがtx t0 tis t1miの場合不明
  if (t!=0 && t!=1 && t!=2 && t!=3 && t!=4 && n!=0 && m!=0){
    switch (m){
    case 1:
    switch (n){
      case 1:
      //所属リンパ節評価不能時の分類
      staging = '分類不能';
      break;
      case 2:
      switch (t){
        case 5:
        staging = 'cStageⅠA1';
        break;
        case 6:
        staging = 'cStageⅠA2';
        break;
        case 7:
        staging = 'cStageⅠA3';
        break;
        case 8:
        staging = 'cStageⅠB';
        break;
        case 9:
        staging = 'cStageⅡA';
        break;
        case 10:
        staging = 'cStageⅡB';
        break;
        case 11:
        staging = 'cStageⅢA';
        break;
      }
      break;
      case 3:
      if (t==10 || t==11)
      {
        staging = 'cStageⅢA';
      } else {
        staging = 'cStageⅡB';
      }
      break;
      case 4:
      if (t==10 || t==11)
      {
        staging = 'cStageⅢB';
      } else {
        staging = 'cStageⅢA';
      }
      break;
      case 5:
      if (t==10 || t==11)
      {
        staging = 'cStageⅢC';
      } else {
        staging = 'cStageⅢB';
      }
      break;
    }
    break;
    case 2:
    staging = 'cStageⅣA';
    break;
    case 3:
    staging = 'cStageⅣA';
    break;
    case 4:
    staging = 'cStageⅣB';
    break;
  }
  }
  else {
    staging = '';
  }
  document.getElementById('output').textContent = '病期分類：c' + ts + ns + ms + ',' + staging;
}
